package com.MeuTeatroPortal.Entidades;

public class Teatro {
	
	
	private int idTeatro;
	private String nomeTeatro;
	private String cnpj;
	private String razaoSocial;
	private String endereco;
	private String cidade;
	private String uf;
	private int cep;
	private String telefone;
	private int ativo;

	public int getIdTeatro() {
		return idTeatro;
	}
	public void setIdTeatro(int idTeatro) {
		this.idTeatro = idTeatro;
	}
	public String getNomeTeatro() {
		return nomeTeatro;
	}
	public void setNomeTeatro(String nomeTeatro) {
		this.nomeTeatro = nomeTeatro;
	}
	public String getCnpj() {
		return cnpj;
	}
	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}
	public String getRazaoSocial() {
		return razaoSocial;
	}
	public void setRazaoSocial(String razaoSocial) {
		this.razaoSocial = razaoSocial;
	}
	public String getEndereco() {
		return endereco;
	}
	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}
	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	public String getUf() {
		return uf;
	}
	public void setUf(String uf) {
		this.uf = uf;
	}
	public int getCep() {
		return cep;
	}
	public void setCep(int cep) {
		this.cep = cep;
	}
	public String getTelefone() {
		return telefone;
	}
	public void setTelefone(String string) {
		this.telefone = string;
	}
	public int getAtivo() {
		return ativo;
	}
	public void setAtivo(int i) {
		this.ativo = i;
	}

	
	
	
	
	
}
